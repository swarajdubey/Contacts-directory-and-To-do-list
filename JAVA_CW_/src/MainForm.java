    private void NewSignup_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewSignup_ActionPerformed
        // TODO add your handling code here:
        first_name=jTextField2.getText(); //gets first name of user
        last_name=jTextField3.getText(); //gets last name of user
        username=jTextField4.getText(); //gets username
        password=jPasswordField2.getText(); //gets password of user
        confirm_password=jPasswordField3.getText(); //gets confirmed password of user
        if(first_name.length()==0 || last_name.length()==0 || username.length()==0 || password.length()==0 || confirm_password.length()==0)
        {
            JOptionPane pane = new JOptionPane();
            pane.setMessage("Kindly fill in all the credentials.");
            JDialog d = pane.createDialog(null, "");
            d.setVisible(true);
            return;
        }
        else
        {
            if(password.length()<8)//password must be atleast 8 characters in length
            {
                JOptionPane pane = new JOptionPane();
                pane.setMessage("Password must be 8 characters in length");
                JDialog d = pane.createDialog(null, "");
                d.setVisible(true);
                return;
            }
            //check if the user confirmed the password corrected
            if(password.contentEquals(confirm_password))
            {
                //sign up successfull
       
                //encrypt the user password in the first place
                EncryptPassword e= new EncryptPassword(password);
                e.findMaxNumber();
                encodedpassword=e.encodePassword(); //this gets the encoded password
                //SQLiteDatabaseConnection sq = new SQLiteDatabaseConnection();
                //DatabaseConnection d = new DatabaseConnection();
                if(sq.addNewUser(first_name, last_name, username, encodedpassword)==true)
                {
                    //add the user name to the task table as a column name
                    sq.addNameAsColumn(username);
                    System.out.println("successfull sign up\n");
                    String name=sq.getPersoneName(username);
                    jLabel1.setText("Welcome "+name+" !");
                    resetCredentialsToNull();
                    CardLayout card= (CardLayout)jPanel4.getLayout();
                    card.next(jPanel4);
                }
                else
                {
                    //inform the user about pre-existing username
                    JOptionPane pane = new JOptionPane();
                    pane.setMessage("Username already exists.");
                    JDialog dialogue = pane.createDialog(null, "");
                    dialogue.setVisible(true);
                    return;
                }
            }
            else{
                //both password not matching during sign up
                JOptionPane pane = new JOptionPane();
                pane.setMessage("Please confirm the password correctly.");
                JDialog d = pane.createDialog(null, "");
                d.setVisible(true);
                return;
            }
        }
        
    }//GEN-LAST:event_NewSignup_ActionPerformed

