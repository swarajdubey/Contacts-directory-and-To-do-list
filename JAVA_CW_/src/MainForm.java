    private void SignIn_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignIn_ActionPerformed
        // TODO add your handling code here:
        
        //check user credentials here
        username=jTextField1.getText();
        password=jPasswordField1.getText();
        if(username.length()==0 || password.length()==0) //if both credentials are not filled
        {
            //inform the user to fill in the credentials
            JOptionPane pane = new JOptionPane();
            pane.setMessage("Please enter credentials in both the fields");
            JDialog d = pane.createDialog(null, "");
            d.setVisible(true);
            return;
            
        }
        
        EncryptPassword e= new EncryptPassword(password); //class which encrypts the password
        e.findMaxNumber();
        encodedpassword=e.encodePassword(); //this gets the encoded password
        
        if(sq.checkLogin(encodedpassword, username)==true) //allow only the correct user to login
        {
            //reset variables to null for next login
            
            //welcome messages
            jLabel4.setText("");
            String name=sq.getPersoneName(username);
            jLabel1.setText("Welcome "+name+" !");
            resetCredentialsToNull();
            CardLayout card= (CardLayout)jPanel4.getLayout();
            card.next(jPanel4);
            
        }
        else{
            //notify user of incorrect username and password field
            JOptionPane pane = new JOptionPane();
            pane.setMessage("Incorrect username or password.");
            JDialog d = pane.createDialog(null, "");
            d.setVisible(true);
            return;
        }
        
    }//GEN-LAST:event_SignIn_ActionPerformed

